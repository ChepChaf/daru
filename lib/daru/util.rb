module Daru
  module Util
    module_function

    def coerce_positions(positions, size, validate: false)
      return positions unless positions.size == 1
      case positions.first
      when Integer
        positions.first
      when Range
        size.times.to_a[positions.first]
      else
        raise ArgumentError, 'Unkown position type.'
      end
    end
  end
end
